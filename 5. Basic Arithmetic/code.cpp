#include <iostream>

using namespace std;

int main(){

    int sum = 6+5; // Sum 
    int dif = 6-5; // Difference
    int prod = 6*5; // Product
    int div = 6/5; // Division
    int mod = 6%5; // Modulus(Remainder)

    cout << sum << "," << dif << "," << prod << "," << div << "," << mod << endl;

    return 0;
}