#include <iostream>

using namespace std;

int main(){

    // Printing in same Line
    cout << "Ah Yes ";
    cout << "The negotiator";

    cout << endl; // Changing line
    // Printing in different Lines
    cout << "Ah Yes " << endl;
    cout << "The negotiator";

    cout << endl; // Changing line
    // Printing in different Lines using escape character
    cout << "Ah Yes \n";
    cout << "The negotiator";
}